package com.telran.lessons.lesson08;

public class UnaryTest {
    public static void main(String[] args) {

        int num = 15;
        num++;
        System.out.println("num = " + num);
        num--;
        System.out.println("num = " + num);
        System.out.println("num = " + ++num);
        System.out.println("num = " + --num);
    }
}
